// var (palavra reservada)
// [NOME DA VARIÉVEL] 
// = (OPERADOR DE ATRIBUIÇÃO) 
// Após o sinal de igual, é o valor atribuido á variável
var numero = 10;

//Tipo de declaração inserido nas últimas versões
let numero2 = 4.56;

// Constantes não podem modificar seu valor após atribuição
const PI = 3.14;

// Modifica o valor da variável
numero = 20;
numero2 = numero + 1;
//PI = 3.14159;

// Chamando a varável numero
// Para imprimir seu valor contido
console.log(numero);
console.log(numero2);
console.log(PI);
